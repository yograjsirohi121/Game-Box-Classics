"""Game entities and enums."""

from enum import Enum


class GameStatus(Enum):
    MENU = "menu"
    PLAYING = "playing"
    PAUSED = "paused"
    GAME_OVER = "game_over"


class RotationDir(Enum):
    CW = 1
    CCW = -1


